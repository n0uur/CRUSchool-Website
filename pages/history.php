              พ.ศ. 2544 – 2551 นายภุชงค์ บุณยรัตนสุนทร ผู้อำนวยการโรงเรียนชลบุรี “สุขบท” ได้ย้ายมาดำรงตำแหน่ง ผู้อำนวยการโรงเรียนชลราษฎรอำรุง ได้มีนโยบายในการดำเนินนำระบบเทคโนโลยีสารสนเทศ คอมพิวเตอร์ในการจัดการเรียนการสอน และใช้ในการบริหาร และได้ดำเนินการแก้ปัญหาการใช้โรงอาหารของนักเรียนซึ่งไม่เพียงพอกับนักเรียนเป็นจำนวนมาก จึงได้ทำโครงการของบประมาณ จากองค์การบริหารส่วนจังหวัดชลบุรี ในการสร้างโรงอาหารใหม่ในปีงบประมาณ 2548
