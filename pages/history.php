            <p class="cryy">
              พ.ศ. 2530 – 2535 นายสมศักดิ์ ศรีสุวรรณ ศิษย์เก่าส้มฟ้า พ.ศ.2500 ได้มาดำรงตำแหน่งผู้อำนวยการโรงเรียน มีความมุ่งมั่นที่จะทำประโยชน์ให้โรงเรียน โรงเรียนจึงได้รับการพัฒนาอย่างมีแบบแผน ด้านอาคารสถานที่มีการกำหนดเขตเป็นสัดส่วน สะอาดเรียบร้อย มีการประดับประดาด้วยสีสันของพันธุ์ไม้ยืนต้น ไม้ดอกไม้ประดับ ร่มรื่น สวยงามมากที่สุดในจังหวัด สภาพของโรงเรียนเป็นที่ชื่นชอบของผู้พบเห็น เป็นศักดิ์ศรีของชาวชลบุรีและลูกส้มฟ้าทุกคน ด้านวิชาการและด้านคุณธรรมจริยธรรมได้พัฒนาขึ้นอย่างชัดเจนเป็นที่ยอมรับ จากรางวัลชนะเลิศต่าง ๆ โรงเรียนชลราษฎรอำรุงได้รับรางวัลพระราชทาน ประจำปีการศึกษา 2533 ในระดับโรงเรียนขนาดใหญ่ เขตการศึกษา 12 และท่านผู้อำนวยการโรงเรียนก็ได้รับรางวัลผู้บริหารดีเด่น
            </p>
