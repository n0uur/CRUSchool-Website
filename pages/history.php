              พ.ศ. 2526 - 2530 โรงเรียนได้เร่งพัฒนาอาคารสถานที่ โดยเฉพาะอาคารเรียน อาคารประกอบและสถานที่ ประกอบกับความต้องการที่จะพัฒนาโรงเรียนให้เป็นศูนย์บริหาร ปฏิบัติการต่าง ๆ และเป็นผู้นำทุกด้านในภาคตะวันออก จึงได้ขอความช่วยเหลือจากชาวชลบุรีและทางราชการ ได้อาคาร 5 เรือนพยาบาล หอสมุด ป้ายชื่อโรงเรียนถาวร รถยนต์โตโยต้าไฮเอชดีเซล โรงอาหาร ถมปรับสนามฟุตบอล
