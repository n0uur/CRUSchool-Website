            <p class="cryy">
              พ.ศ. 2535 – 2540 นายชะนะ กมลานนท์ ศิษย์เก่าส้มฟ้า เตรียมอุดมศึกษา รุ่น 11 มาดำรงตำแหน่งผู้อำนวยการโรงเรียน ได้พัฒนาโรงเรียนอย่างต่อเนื่อง ด้วยความวิริยะอุตสาหะและเต็มกำลังความสามารถ ทำให้โรงเรียนชลราษฎรอำรุง มีความเจริญก้าวหน้าเพิ่มขึ้น ได้นำเทคโนโลยีสมัยใหม่ มาใช้ในการพัฒนาองค์กร และใช้ในการเรียนการสอนได้ประสานกับสมาคมศิษย์เก่าชลราษฎรอำรุง สมาคมผู้ปกครองและครูโรงเรียนชลราษฎรอำรุง ชุมชน ตลอดจนถึงผู้ปกครอง เพื่อนำทรัพยากรเข้ามาพัฒนาโรงเรียน ช่วยส่งเสริมสนับสนุนกิจกรรมต่าง ๆ ของโรงเรียนอย่างหลากหลาย เพื่อให้นักเรียนได้รับประสบการณ์ตรง เป็นการเพิ่มศักยภาพในการเรียนการสอน ให้มีความพร้อมที่จะเป็นทรัพยากรมนุษย์ที่มีคุณภาพของสังคมสืบไป
