            <p class="cryy">
              ปี 2511 โรงเรียนชลราษฎรอำรุง เข้าโครงการมัธยมแบบประสมรุ่น 4 ซึ่งจะต้องเปิดสอน หลักสูตร คมส.1 ในปีการศึกษา 2512 กรมวิสามัญจึงส่งเจ้าหน้าที่มาสำรวจ เพื่อดำเนินการก่อสร้างอาคารเรียน และอาคารประกอบให้เป็นไปตามโครงการ ผลการสำรวจปรากฏว่า เนื้อที่ของโรงเรียนมีอยู่เพียง 13 ไร่ ไม่เพียงพอกับการขยายโรงเรียนจังหวัดชลบุรี นายนารถ มนตเสวี ผู้ว่าราชการจังหวัดชลบุรีในขณะนั้น ได้มองเห็นความสำคัญและประโยชน์ของการศึกษา จึงได้พิจารณาจัดสรรที่ดินบริเวณสนามบินกองทัพอากาศเดิมประมาณ 67 ไร่ บริเวณที่แยกหนองข้างคอก ริมถนนสุขุมวิท ติดกับถนนพระยาสัจจาปัจจุบัน ที่ดินบริเวณนี้อยู่ในตำบลบ้านสวนฝั่งตรงข้ามเยื้องกับโรงพยาบาลชลบุรี
            </p>
