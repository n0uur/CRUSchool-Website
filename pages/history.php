              3 มิถุนายน 2484 เป็นวันแรกที่ย้ายโรงเรียน และนักเรียนจากสถานที่เดิม ซึ่งเรียนกันมาตั้งแต่ พ.ศ. 2460 มาเปิดใหม่ ณ สถานที่โรงเรียนริมถนนสุขุมวิท (สาย 27) บนเนื้อที่ประมาณ 13 ไร่ ซึ่งเป็นที่ดินของวัดอรัญญิกาวาส เพื่อขยายโรงเรียนให้กว้างขวางขึ้น หลังจากนั้นโรงเรียนได้พัฒนาการเรียนการสอน โดยเปิดสอนระดับเตรียมอุดมศึกษา และเปลี่ยนชื่อโรงเรียนใหม่ เป็น “โรงเรียนชลราษฎรอำรุง” ในปัจจุบันชาวส้มฟ้า จึงถือว่า วันที่ 3 มิถุนายน ของทุกปี เป็นวันเกิดของโรงเรียน            </p>
